import 'package:movies_app/src/common/constants.dart' as Constants;

class Actor {
  int castId;
  String character;
  String creditId;
  int gender;
  int id;
  String name;
  int order;
  String profilePath;

  Actor({
    this.castId,
    this.character,
    this.creditId,
    this.gender,
    this.id,
    this.name,
    this.order,
    this.profilePath,
  });

  Actor.fromJsonMap(Map<String, dynamic> json) {
    castId = json['cast_id'];
    character = json['character'];
    creditId = json['credit_id'];
    gender = json['gender'];
    id = json['id'];
    name = json['name'];
    order = json['order'];
    profilePath = json['profile_path'];
  }

  String getPosterImage() {
    if (profilePath != null) {
      return Constants.URL_POSTER_IMG + '$profilePath';
    } else {
      return Constants.NO_IMAGE_WEB;
    }
  }
}
